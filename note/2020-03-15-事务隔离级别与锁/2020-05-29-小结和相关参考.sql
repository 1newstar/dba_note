分析死锁一般要从死锁日志中获取如下信息

	1、加锁发生在主键还是辅助索引
	2、加锁的模式是什么
	3、是单行还是多行加锁
	4、触发死锁事务最后的语句
	5、死锁信息中事务顺序是怎么样的
