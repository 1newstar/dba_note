MongoDB使用开源协议SSPL。

AGPL：一种主流开源协议。MySQL和MongoDB 4.0用的都是这个协议。
SSPL： AGPL 基础之上加了一个专门针对公有云厂商（如阿里云，腾讯云，亚马逊等）的特别条款。

如果你是99.99999%的非公有云厂商用户，你可以像AGPL一样（MySQL一样）使用MongoDB
如果你是0.00001%的公有云厂商用户，你不可以用SSPL协议的MongoDB在云里提供MongoDB托管服务。


