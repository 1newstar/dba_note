

步骤简述：
	0. 先以单机的形式启动
		
	1. 在副本集的从节点上进行一致性快照备份  --使用 oplog 参数
	
	2. 新的从节点上进行一致性快照恢复，仅仅对数据部分进行恢复，暂时不要对oplog进行恢复 -- 使用 oplogRelay 参数 
	
	3. 初始化oplog.rs集合，并恢复oplog记录  -- 填充 oplog，用于断点续传

	4. 初始化local数据库的其他两个集合db.replset.election，db.system.replset  -- 在4.2版本中, 我省略了这个步骤 

	5. 修改数据库配置并重启数据库（这一步操作前实例不开启认证模式、复制集的配置）,
	
	6. 用rs.add("HOST_NAME:PORT")命令将从节点添加进集群

	7. 用rs.status()观察同步状态并校验数据的完整和一致性     
			   
		
		