MGR的优点:

    高一致性，基于原生复制及paxos协议的组复制技术.

    高容错性，有自动检测机制，当出现宕机后,会自动剔除问题节点,其他节点可以正常使用,当不同节点产生资源争用冲突时,会按照先到先得处理,并且内置了自动化脑裂防护机制.

    高扩展性，可随时在线新增和移除节点,会自动同步所有节点上状态，直到新节点和其他节点保持一致，自动维护新的组信息.

    高灵活性，以插件形式安装,有两种模式:单主模式和多主模式
	
MGR缺点和限制:

    对网络稳定性要求很高,至少是同机房做。(需要低延迟，高带宽的网络。同城跨机房IDC)
	
    稳定性相对较弱。

    不支持binlog checksum

    不支持savepoint
  
    不支持gap locks    #待验证

    不支持lock tables, unlock tables

    多主模式不支持多节点同时对一个表进行DDL vs DDL/DML

    多主模式不支持SERIALIZABLE隔离级别

    多主模式不支持多级关联外键
	
	