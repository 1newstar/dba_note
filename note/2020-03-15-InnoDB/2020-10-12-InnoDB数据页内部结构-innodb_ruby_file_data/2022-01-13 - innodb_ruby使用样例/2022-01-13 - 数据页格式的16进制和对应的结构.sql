
1. File Header 的 38 bytes


	0000c000  19 79 71 58 00 00 00 03  ff ff ff ff ff ff ff ff  |.yqX............|
	0000c010  00 00 00 10 bd 15 80 0e  45 bf 00 00 00 00 00 00  |........E.......|
	0000c020  00 00 00 00 12 a6 00 1a  0d 5c 80 66 00 00 00 00  |.........\.f....|
	
	19 79 71 58              页的 checksum 值
	00 00 00 03              页的偏移量, 从0开始
	ff ff ff ff   		     前一个页, 因为只有当前一个数据页, 用 0xffffffff 表示
	ff ff ff ff   			 下一个页, 因为只有当前一个数据页, 用 0xffffffff 表示
	00 00 00 10 bd 15 80 0e  数据页的LSN
	45 bf         			 页类型, 0x45bf 代表数据页
	00 00 00 00 00 00 00 00  独立表空间，该值为0
	00 00 12 a6   			 表空间的 space id


2. File Trailer

	0000fff0  01 e9 01 65 00 e1 00 63  19 79 71 58 bd 15 80 0e  |...e...c.yqX....|
	ce 10 a4 63   checksum值, 该值通过 checksum 函数与 file header 部分的checksum 值进行比较
	bd 15 80 0e   值 与 file header 部分数据页的 LSN后4个字节相等.
	
3. Page Header
	
	0000c020  00 00 00 00 12 a6 00 1a  0d 5c 80 66 00 00 00 00  |.........\.f....|
	0000c030  0d 41 00 02 00 00 00 64  00 00 00 00 00 00 00 00  |.A.....d........|
	0000c040  00 00 00 00 00 00 00 00  89 53 00 00 12 a6 00 00  |.........S......|
	0000c050  00 02 00 f2 00 00 12 a6  00 00 00 02 00 32 01 00  |.............2..|	
	

	00 1a	 PAGE_N_DIR_SLOTS  	 	代表 Page Directory 有26个槽,每个槽占用2byte;  这部分的内容可以 通过 innodb_space page-directory-summary 获取
	0d 5c    PAGE_HEAP_TOP      	代表 Free Space开始位置的偏移量，即 0xc000+0x0d5c=0xcd5c 处开始
	80 66    PAGE_N_HEAP        	初始值为  0x0802, 0x8066 - 0x8002 = 0x64, 代表该页中实际的记录有100条.
	00 00	 PAGE_FREE          	0x0000 代表可重用的空间首地址, 因为这里没有进行过任何删除操作, 这里的值依然为 0 .
	00 00	 PAGE_GARBAGE       	0x0000 代表删除的记录字节为 0, 因为这里没有进行过任何删除操作, 这里的值依然为 0 .
	0d 41    PAGE_LAST_INSERT   	0x0d41 代表页最后插入的位置的偏移量
	00 02    PAGE_DIRECTION     	0x0002 因为通过自增长的方式进行行记录的插入, 所以 PAGE_DIRECTION 的方向是向右即 PAGE_RIGHT
	00 63    PAGE_N_DIRECTION   	0x0063 表示一个方向连接插入记录的数量, 因为我们是自增长的方式插入了 100行记录, 因此该值为 99
	00 64    PAGE_N_RECS        	0X0064 表示该页的行记录数为 100;
	00 00 00 00 00 00 00 00 PAGE_MAX_TRX_ID
	00 00    PAGE_LEVEL         	0X00, 代表该页为叶子节点; 因为数据量目前较少, 因此当前  B+ TREE 索引只有一层. B+ 树叶子层总是为  0x00;  
	00 00 00 00 00 00 89 53    PAGE_INDEX_ID   索引ID，表示当前页属于哪个索引
	00 00 12 a6 00 00 00 02 00 f2   PAGE_BTR_SEG_LEAF
	00 00 12 a6 00 00 00 02 00 32   PAGE_BTR_SEG_TOP			
	
	
4. Infimum + Supremum Records
	
	0000c050  00 02 00 f2 00 00 12 a6  00 00 00 02 00 32 01 00  |.............2..|
	0000c060  02 00 1b 69 6e 66 69 6d  75 6d 00 05 00 0b 00 00  |...infimum......|
	0000c070  73 75 70 72 65 6d 75 6d  00 00 00 10 00 21 00 00  |supremum.....!..|
	
	0000c050 到 0000c070, 存放的是这两个伪行记录, 在InnoDB存储引擎中, 设置伪行只有一个列, 且类型是 char(8);
	
	Infimum 伪行记录 
		01 00 02 00 1b                   # 记录头信息 即  recorder header
		69 6e 66 69 6d 75 6d 00          只有一个列的伪行记录, 记录内容就是 Infimum, 类型是 char(8) 
	
	supremum 伪行记录
		05 00 0b 00 00		             # 记录头信息 即  recorder header
		73 75 70 72 65 6d 75 6d          只有一个列的伪行记录, 记录内容就是 supremum, 类型是 char(8)
	
5. User Record

	解析第一个行记录
	
	0000c070  73 75 70 72 65 6d 75 6d  00 00 00 10 00 21 00 00  |supremum.....!..|
	0000c080  00 01 00 00 00 12 d9 14  a1 00 00 00 05 01 10 67  |...............g|
	0000c090  67 67 67 67 67 67 67 67  67 00 00 00 18 00 21 00  |ggggggggg.....!.|
	0000c0a0  00 00 02 00 00 00 12 d9  15 a1 00 00 00 05 01 10  |................|
	
						 # 变长字段列表 表中没有变长字段
	00  				 # NULL标志位	该行记录没有列为NULL
	00 00 10 00 21		 # 记录头信息，固定 5 byte
	00 00 00 01          # 我们建的表有主键，这里的ROWID即为列a的值1
	00 00 00 12 d9 14    # 事务ID, 占用 6 byte     
	a1 00 00 00 05 01 10 # 回滚指针, 占用 7 byte
	67 67 67 67 67 67 67 67 67 67    # 列a(列1) 数据为字符串 'gggggggggg'
