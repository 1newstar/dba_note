

额外的占用存储空间


Boyka:
这是一条记录的格式   一条记录会包含null值列表   每个默认是null的字段都有对应的null标记位（bit）

Boyka:
一个表有30个字段   那么都是默认null  对应的null位就是  30个bit  也就是需要4个字节    假设有2000万条数据  就需要额外存储  20000万*4个字节


4*8  不是32个bit   30个bit不得4个字节存储吗

1个字节=8个bit


空字符不占用存储空间。

select 20000000 * 4;

mysql> select 20000000 * 4;
+--------------+
| 20000000 * 4 |
+--------------+
|     80000000 |
+--------------+
1 row in set (0.00 sec)

80000000 byte = 76 MB


