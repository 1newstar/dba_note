char(n) ：
	
	固定长度类型
	从存储的角度：
		订阅 char(10)，当你输入”abc”三个字符的时候，它们占的空间还是 10 个字节，其他 7 个是空字节。
	优点：效率高；
	缺点：占用空间；
	适用场景：存储密码的 md5 值，固定长度的，使用 char 非常合适。 

varchar(n) ：

	可变长度，无论是什么字节字符集，都是变长的，即都存储变长数据和变长字段长度列表。
	从存储的角度：
		使用了多少个字符串长度，那么实际存储就占用几个字节;
	
	varchar(10) 中 10 的涵义最多存放 10 个字符，varchar(10) 和 varchar(20) 存储 'hello' 字符串所占空间一样
	
所以，从空间上考虑 varcahr 比较合适；
从效率上考虑 char 比较合适，二者使用需要权衡。


