
1. 作用

	解决数据页部分写失效的问题。
	
2. 工作机制

	脏页在刷盘的时候，先拷贝到内存到double write buffer中，然后分为2次写，
	每次以1MB的大小写入到共享表空间的 double write 中，再把数据页写入到对应的数据表空间中。
	
	此时如果出现断电异常，可能会出现数据页部分写失效，在崩溃恢复过程中，应用double write中的数据页到内存，重新做一遍数据页刷盘的操作，就完成了数据恢复的过程。
	
	
	-----------------------------------------------------------------------------
	
	简单来说，在MySQL InnoDB存储中，脏页列表的刷新，会先顺序地写入到doublewrite，然后再随机写入到对应的数据页中。