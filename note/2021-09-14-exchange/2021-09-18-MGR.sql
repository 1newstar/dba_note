

5.7.17 版本开始支持MGR组复制功能。

优点：
	
	1. 不依赖外部组件就可以实现 故障自动切换、手动(在线)切换、多点写入
	
	2. 数据的强一致性：
		事务的提交，必须经过组内大多数节点(n/2+1)决议并通过，才能得以提交
	


缺点：
	1. 路由/驱动没有做好。
		-- 主库宕机，会自动完成新的组复制架构，但是应用连接还是连接到了宕机的主库上了。
		-- 需要中间件来实现。
	
		MongoDB 的驱动就做得很好，支持链接多数据库节点配置
	
	2. 不支持日志节点也就是仲裁节点

限制：
	1. 表的存储引擎必须是InnoDB
	2. 要开启GTID
	3. 表要有主键
	4. 基于binlog的逻辑复制，因此要开启binlog，并且binlog格式为row
	
	