


用法规则：
	substring_index("待截取有用部分的字符串", "截取数据依据的字符", "截取字符的位置")
	
 
mysql> select SUBSTRING_INDEX('7654,7698,7782',',',-1);
+------------------------------------------+
| SUBSTRING_INDEX('7654,7698,7782',',',-1) |
+------------------------------------------+
| 7782                                     |
+------------------------------------------+
1 row in set (0.00 sec)



mysql>  select SUBSTRING_INDEX('7654,7698,7782',',',1); 
+-----------------------------------------+
| SUBSTRING_INDEX('7654,7698,7782',',',1) |
+-----------------------------------------+
| 7654                                    |
+-----------------------------------------+
1 row in set (0.00 sec)



mysql> select SUBSTRING_INDEX('7654,7698,7782',',',2); 
+-----------------------------------------+
| SUBSTRING_INDEX('7654,7698,7782',',',2) |
+-----------------------------------------+
| 7654,7698                               |
+-----------------------------------------+
1 row in set (0.00 sec)


mysql> select SUBSTRING_INDEX(SUBSTRING_INDEX('7654,7698,7782', ',', 2), ',', -1);
