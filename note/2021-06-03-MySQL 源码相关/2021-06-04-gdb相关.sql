
常用的参数命令：

	info threads：查看全部线程

	thread n：指定某个线程

	b：在某处打断点

	c：继续往下走

	s：执行一行代码，如果代码函数调用，则进入函数

	n：执行一行代码，函数调用不进入

	p：打印某个变量值

	list：打印代码的文本信息

	bt：查看某个线程的栈帧

	info b：查看当前所有断点信息
	
	
	
