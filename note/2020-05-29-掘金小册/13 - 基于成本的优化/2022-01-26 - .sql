需要回表的记录数
	
	相隔不大于10个页面
	
		如果 区间最左记录 和 区间最右记录 相隔不太远 (在MySQL 5.7.21这个版本里，只要相隔不大于10个页面即可)，那就可以精确统计出满足 key2 > 10 AND key2 < 1000 条件的二级索引记录条数。
	
	相隔大于10个页面
	
		只沿着区间最左记录向右读10个页面，计算平均每个页面中包含多少记录，然后用这个平均值乘以区间最左记录和区间最右记录之间的页面数量就可以了。


