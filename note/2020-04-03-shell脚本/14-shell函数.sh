1. shell 函数简介
	1. 可以带 function fun() 定义, 也可以直接 fun() 定义, 不带任务参数
	2. 参数返回, 可以显示加: return 返回, 如果不加, 将以最后一条命令运行结果, 作为返回值

2. shell 函数的定义格式
	[ function ] funname [()]

	{

		action;

		[return int;]

	}
