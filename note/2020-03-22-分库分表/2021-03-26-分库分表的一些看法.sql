


关于分库分表的一些看法：

	大部分的业务场景，几乎都是日志表的体积量相对较大，如果真的要分库分表，对日志表做分表就好了，也可以用拆分的思路，把一些独立的日志大表，用MongoDB来存储。
	日志表，数据保留半年-1年，历史数据可以迁移到历史库中。
	
	维护分布式架构比维护集中式架构，复杂得多。
	
	
	
