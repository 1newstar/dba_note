


3.3 逻辑表（table）
	3.3.1 逻辑表
		既然有逻辑库，那么就会有逻辑表，分布式数据库中，对应用来说，读写数据的表就是逻辑表。 
		逻辑表，可以是数据切分后，分布在一个或多个分片库中，也可以不做数据切分，不分片，只有一个表构成。
		
Hash分片

   Hash分片其实企业级应用尤其广泛，我觉得很的一个原因是通过这种数据路由的方式，得到的数据情况是基本可控的，和业务的关联起来比较直接。很多拆分方法都是根据mod方法来平均分布数据。
   
Mycat 中，旧系统数据如何迁移到 Mycat 中？
	答：旧数据迁移目前可以手工导入，在 mycat 中提取配置好分配规则及后端分片数据库，然后通过 dump
	或 loaddata 方式导入，后续 Mycat 就做旧数据自动数据迁移工具		